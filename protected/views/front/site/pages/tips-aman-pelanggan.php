•	Tips Aman
◦	Usahakan untuk tidak mentransfer uang secara langsung sebelum ada transaksi atau kesepakatan.
◦	Pastikan untuk mengecek lokasi bisnis pebisnis terlebih dahulu. Jika pemilik bisnis dirasa tidak nyaman untuk diteruskan, segera putuskan untuk lanjut atau tidak
◦	Jika pebisnis meminta system pembayaran transfer:
1.	Lakukan pengecekan dokumen pebisnis terlebih dahulu seperti fotocopy KTP, SIM dan nomor rekening penjual. Apakah ketiga dokumen tersebut memiliki nama yang sama atau tidak, asli atau tidak (ada data-data di dokumen yang dirubah), apakah diantara dokumen tersebut ada yang meragukan atau tidak.
2.	Jika pebisnis memiliki account social media, lakukan pengecekan terhadap account pebisnis, sudah berapa lama dia memiliki account tersebut, berapa banyak teman/koneksi yang dimiliki dan informasi-informasi lain yang bisa meyakinkan anda.
◦	Hati-hati, Jangan mudah terpancing jika pebisnis memberikan informasi atau janji-janji yang berlebihan (misal: harga terlalu rendah, alasan banyak orang lain yang berminat dan anda diminta harus segera transfer DP, dll).

